// Trả về sticker ngẫu nhiên
function getRandomStickerId() {
  return STICKER_IDS[Math.floor(Math.random() * STICKER_IDS.length)];
}

// Quản lý interval riêng cho từng nhóm
const autoStickerIntervals = new Map(); // threadId => intervalId

// Hàm xử lý gửi sticker tự động
export async function handleSendStickerCommand(
  api,
  message,
  groupSettings = readGroupSettings(),
) {
  const content = removeMention(message);
  const status = content.split(" ")[1]?.toLowerCase();
  const threadId = message?.threadId || message?.data?.idTo;

  if (!threadId) {
    // console.warn(`[SendSticker] Missing threadId`);
    return;
  }

  // console.log(`[SendSticker] Processing for thread ${threadId}, current settings:`, groupSettings[threadId]);

  if (!groupSettings[threadId]) {
    groupSettings[threadId] = {};
  }

  let newStatus;
  if (status === "on") {
    groupSettings[threadId].sendstk = true;
    newStatus = "bật";
  } else if (status === "off") {
    groupSettings[threadId].sendstk = false;
    newStatus = "tắt";
  } else {
    groupSettings[threadId].sendstk = !groupSettings[threadId].sendstk;
    newStatus = groupSettings[threadId].sendstk ? "bật" : "tắt";
  }

  // Lưu groupSettings vào file
  try {
    writeGroupSettings(groupSettings);
    // console.log(`[SendSticker] Saved settings for thread ${threadId}:`, readGroupSettings()[threadId]);
  } catch (error) {
    // console.error(`[SendSticker] Error saving group settings: ${error.message}`);
  }

  // Quản lý interval gửi sticker
  if (groupSettings[threadId].sendstk) {
    // Bật: Bắt đầu interval nếu chưa có
    if (!autoStickerIntervals.has(threadId)) {
      const intervalId = setInterval(async () => {
        const currentSettings = readGroupSettings();
        if (!currentSettings[threadId]?.sendstk) {
          // Dừng interval nếu sendstk bị tắt
          clearInterval(autoStickerIntervals.get(threadId));
          autoStickerIntervals.delete(threadId);
          // console.log(`[SendSticker] Stopped interval for thread ${threadId}`);
          return;
        }

        const stickerId = getRandomStickerId();
        const sticker = { id: stickerId, cateId: 1, type: 1 };
        try {
          await api.sendSticker(
            sticker,
            threadId,
            MessageType.GroupMessage,
            60000,
          );
          // console.log(`[SendSticker] Sent sticker ${stickerId} to thread ${threadId}`);
        } catch (err) {
          // console.error(`[SendSticker] Error sending sticker to thread ${threadId}:`, err);
        }
      }, 60 * 1000); // Mỗi 1 phút

      autoStickerIntervals.set(threadId, intervalId);
      // console.log(`[SendSticker] Started interval for thread ${threadId}`);
    }
  } else {
    // Tắt: Dừng interval nếu có
    if (autoStickerIntervals.has(threadId)) {
      clearInterval(autoStickerIntervals.get(threadId));
      autoStickerIntervals.delete(threadId);
      // console.log(`[SendSticker] Stopped interval for thread ${threadId}`);
    }
  }

  // Gửi thông báo trạng thái
  const caption = `Đã ${newStatus} chức năng gửi sticker tự động vào nhóm này!`;
  try {
    await sendMessageStateQuote(
      api,
      message,
      caption,
      groupSettings[threadId].sendstk,
      300000,
    );
    // console.log(`[SendSticker] Sent state quote for thread ${threadId}: ${caption}`);
  } catch (error) {
    // console.error(`[SendSticker] Error sending state quote: ${error.message}`);
  }
}
